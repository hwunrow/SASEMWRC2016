<div id="about-section" style="color:black;">
  <div class="container">
    <div class="section-title text-center wow fadeInDown">
      <h2><strong> Mission </strong></h2>
      <hr>
      <div class="clearfix"></div>
      <p>The vision of our conference is to create a stronger community by promoting diversity in leadership.
      We believe that an essential part of an organization’s success depends on its ability to embrace diversity.
      When an organization actively assesses its handling of diversity issues, many benefits follow. 
      These include a variety of viewpoints, teamwork, and more effective execution, resulting in higher productivity and profit. 
      Thus, we wish to educate our participants on the importance of diversity.</p>
    </div>
    <div class="row">
      <div class="col-md-12 wow fadeInRight">
          <h4>Goals</h4>
          <p><strong>To develop conference workshops that will educate students on the importance of a variety of topics, including diversity and inclusion efforts.</strong></p>
          
          <p><strong>To empower participants, using their life experiences to reach their full potential and make an impact on the world.</strong></p>
           
          <p><strong>To create a more diverse and close-knit community.</strong></p>
      </div>
      <div class="col-md-12 wow fadeInRight">
          <h4>What We Do</h4>
          <p>These goals will be accomplished through invitation of non-SASE-affiliated representatives, corporate involvement, technology demonstrations, presentation of the SASE Junior and other pilot programs, a Corporate Networking session, and a wide range of fun and educational workshops. These will prepare our attendees for future careers, as well as build the connections necessary to succeed as professionals. The 2016 SASE Midwest Regional Conference will serve as a catalyst, acting as a public forum for exchanging ideas, connecting different chapters with each other, and introducing university campuses to high schools currently without a SASE Junior chapter, and vice versa.</p>
      </div>
